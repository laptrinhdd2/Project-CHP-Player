package com.nxchien.chpmusic.ui.widget;

public class C_RectF {

    public float x;
    public float y;
    public float width;
    public float height;

    public C_RectF(float left, float top, float width, float height) {
        this.x = left;
        this.y = top;
        this.width = height;
        this.height = height;
    }
}